#include<iostream>
using namespace std;
int main(){
  int n1, n2;
  cout << "Enter first number: ";
  cin >> n1;
  cout << "Enter second number: ";
  cin >> n2;
  // Operations like --> + , - , / , * , %
  cout << "SUM = " << n1 + n2 << endl;
  cout << "SUBTRACT = " << n1 - n2 << endl;
  cout << "MULTIPLY = " << n1 * n2 << endl;
  cout << "DIVIDE " << n1 / n2 << endl;
  cout << "MODULO = " << n1 % n2 << endl;

}